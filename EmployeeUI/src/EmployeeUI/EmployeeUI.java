    private void CalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateActionPerformed
if (ftRadio.isSelected()) {
         
             dRate = Double.parseDouble (deductionRateField.getText());
             yearlySalary = Double.parseDouble(yearlySalaryField.getText());  
             
             nIncome = yearlySalary * (1 - (dRate / 100));
             netIncomeField.setText(Double.toString(nIncome));
        
        } 
         else {
            if (ptRadio.isSelected()) {
            dRate = Double.parseDouble (deductionRateField.getText());    
            hourlyWage = Double.parseDouble (hourlyWageField.getText());
            hrsPerWeek = Float.parseFloat (hrsPerWeekField.getText());
            weeksPerYear = Integer.parseInt (weeksPerYearField.getText());        
            nIncome = (hourlyWage * hrsPerWeek * weeksPerYear) * (1 - (dRate / 100));
            netIncomeField.setText(Double.toString(nIncome));


            }
            else{
            netIncomeField.setText("Please Select Occupation Type");
            }   
            
         }            // TODO add your handling code here:
        
        
    }//GEN-LAST:event_CalculateActionPerformed

