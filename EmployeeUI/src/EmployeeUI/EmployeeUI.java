    private void modifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyButtonActionPerformed
        tempEmployee = hTable.search(Integer.parseInt(searchField.getText()));
                // TODO add your handling code here:
        if (tempEmployee == null ){
            outputField.setText("Cannot Modify" + "/n" + " Employee with that employee number does not exist.");
            
        }
        else{
            if (tempEmployee instanceof PartTime){ 
           tempPT = (PartTime) tempEmployee;
             
         firstNameField.setText(tempPT.firstName);
         middleNameField.setText(tempPT.middleName);
         lastNameField.setText(tempPT.lastName);
         employeeNumberField.setText(Integer.toString(tempPT.employeeNumber));
         deductionRateField.setText(Double.toString(tempPT.deductionRate));
         ptRadio.setSelected(true);
         if (gender == "Male") {
             genderBox.setSelectedItem("Male");
         }
         else{
             genderBox.setSelectedItem("Female");
         }
         hourlyWageField.setText(Double.toString(tempPT.hourlyWage));
         hrsPerWeekField.setText(Float.toString(tempPT.hrsPerWeek));
         weeksPerYearField.setText(Integer.toString(tempPT.weeksPerYear));
         netIncomeField.setText(Double.toString (tempPT.netIncome));
         hTable.delete(tempPT.employeeNumber);
            }
           if (tempEmployee instanceof FullTime){
                tempFT = (FullTime) tempEmployee;
             
         firstNameField.setText(tempFT.firstName);
         middleNameField.setText(tempFT.middleName);
         lastNameField.setText(tempFT.lastName);
         employeeNumberField.setText(Integer.toString(tempFT.employeeNumber));
         deductionRateField.setText(Double.toString(tempFT.deductionRate));
         if (gender == "Male") {
         genderBox.setSelectedItem("Male");
         }
         else{
             genderBox.setSelectedItem("Female");
         }
         ftRadio.setSelected(true);
         hourlyWageField.setText(Double.toString(tempFT.yearlySalary));
         netIncomeField.setText(Double.toString (tempFT.netIncome));
         hTable.delete(tempFT.employeeNumber);
           }
        }
    }//GEN-LAST:event_modifyButtonActionPerformed

