    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        
        
        fName = firstNameField.getText();
        mName = middleNameField.getText();
        lName = lastNameField.getText();
        eN = Integer.parseInt(employeeNumberField.getText());
        
       EmployeeInfo result = hTable.search(eN);
       if (result == null){
                
        dRate = Double.parseDouble(deductionRateField.getText());
        gender = (String)genderBox.getSelectedItem( );
       
         if (ftRadio.isSelected()) {
         
             {
             yearlySalary = Double.parseDouble(yearlySalaryField.getText());  
             }
             nIncome = yearlySalary * (1 - (dRate / 100));
             FullTime employee = new FullTime(eN, fName, mName, lName, gender, dRate, yearlySalary, nIncome);
             
             hTable.add(employee);
        
        } 
         else {
            if (ptRadio.isSelected()) {
                
            hourlyWage = Double.parseDouble (hourlyWageField.getText());
            hrsPerWeek = Float.parseFloat (hrsPerWeekField.getText());
            weeksPerYear = Integer.parseInt (weeksPerYearField.getText());        
            nIncome = (hourlyWage * hrsPerWeek * weeksPerYear) * (1 - (dRate / 100));
            PartTime employee = new PartTime(eN, fName, mName, lName, gender, dRate, hourlyWage, hrsPerWeek, weeksPerYear, nIncome);
            hTable.add(employee);

            }
         }
         firstNameField.setText("");
         middleNameField.setText("");
         lastNameField.setText("");
         employeeNumberField.setText("");
         deductionRateField.setText("0");
         ftRadio.setSelected(false);
         ptRadio.setSelected(false);
         yearlySalaryField.setText("0");
         hourlyWageField.setText("0");
         hrsPerWeekField.setText("0");
         weeksPerYearField.setText("0");
         netIncomeField.setText("");
       }
       else{
           outputField.setText("An employee with that employee number already exists. \n Please enter a new number.");
          
                   
       }
    }//GEN-LAST:event_addButtonActionPerformed
                                  
